class SwitchDemo{

	public static void main(String[] args){
	
		int num = 28;

		if(num%2 == 0){
		
			System.out.println(num + " is an even number");

		}else if(num%2 == 1){
		
			System.out.println(num + " is an odd number");

		}else{
		
			System.out.println("Enter a correct number");
		}
	}
}
