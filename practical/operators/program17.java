class C2W{
	public static void main(String[] args){
		int football = 2;

		System.out.println(football+=2); //4

		System.out.println(--football);//3

		System.out.println(football%3);//0

		System.out.println(football); //3
		int players = 3;
		System.out.println(football == players); //true

		int sum = football + players; //6
		System.out.println(sum);
		int avg = sum / 2;
		System.out.println(avg);
	}
}
